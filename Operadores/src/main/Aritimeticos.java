package main;

public class Aritimeticos {

	public static void main(String[] args) {
		int n1=10, n2=9, n3 = 5;
		double media = 0;
		
		//adição
		media = n1 + n2 + n3;
		System.out.println("Adição " + media);
		//subtração
		media = n1 - n2 - n3;
		System.out.println("Subtração " +media);
		//divisão
		media = n1 / n2 / n3;
		System.out.println("Divisão " +media);
		//multiplicação
		media = n1 * n2 * n3;
		System.out.println("Multiplicação " +media);
		//módulo ou resto
		media = n1 % n2 % n3;
		System.out.println("Resto " +media);
	}

}
